package com.hilive.domain;

import java.util.List;

/**
 * Created by ttc on 2018/4/23.
 */
public class PageData<T> {
    private int total;
    private List<T> list;

    public int getTotal() {
        return total;
    }

    public void setTotal(int total) {
        this.total = total;
    }

    public List<T> getList() {
        return list;
    }

    public void setList(List<T> list) {
        this.list = list;
    }
}
